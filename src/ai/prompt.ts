// 🧠 1. Фактларни ажратиб олувчи
export function getFactExtractionPrompt(message: string): string {
  return `
Ты анализатор фактов. Извлеки из следующего сообщения парня важные факты о нем (хобби, где живет, интересы, цели).
Если фактов нет, напиши строго одно слово: "ЙЎҚ".
Сообщение: "${message}"
Факт (или ЙЎҚ):
  `.trim();
}

